
lista=[5,7,2,9,4,1,3]
# A) Tamanho da lista
print("O tamanho da lista é de: ")
print(len(lista))

# B) Maior valor da lista
print("O maior valor da lista: ")
print(max(lista))

# C) Menor valor da lista
print("O menor valor da lista: ")
print(min(lista))

# D) Soma de todos os elementos da lista
print("A soma de todos os elementos da lista é igual a: ")
print(sum(lista))

# E) Lista em ordem crescente
print("Lista em ordem crescente: ")
lista.sort()
print(lista)

# F) Lista em ordem decrescente
print("Lista em ordem decrescente: ")
lista.reverse()
print(lista)





